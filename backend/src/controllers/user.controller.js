const User = require("../models/user.model");

module.exports.editAccount = async (req, res) => {
  try {
    const {
      username,
      fullName,
      email
    } = req.body;
    const userId = req.user.id;

    // Kiểm tra username đã tồn tại ở user khác
    const usernameExist = await User.findOne({
      _id: {
        $ne: userId
      },
      username
    });

    if (usernameExist) {
      return res.status(400).json({
        message: "Username này đã tồn tại"
      });
    }

    // Kiểm tra email đã tồn tại ở user khác
    const emailExist = await User.findOne({
      _id: {
        $ne: userId
      },
      email
    });

    if (emailExist) {
      return res.status(400).json({
        message: "Email này đã tồn tại"
      });
    }

    // Cập nhật user
    const updated = await User.updateOne({
      _id: userId
    }, {
      username,
      fullName,
      email
    });

    return res.status(200).json({
      message: "Cập nhật tài khoản thành công",
      updated
    });

  } catch (error) {
    console.log(error);
    return res.status(500).json({
      message: "Lỗi server khi cập nhật tài khoản"
    });
  }
};


module.exports.uploadAvatar = async (req, res) => {
  try {
    const userId = req.user.id;
    const avatar = req.body.avatar;
    if (!avatar) {
      return res.status(400).json({
        message: "Bạn chưa upload avatar"
      })
    }
    // save avatar
    const user = await User.findByIdAndUpdate(
      userId, {
        avatar
      }, {
        new: true
      } // trả về user sau khi update
    );

    return res.status(200).json({
      message: "Avatar updated",
      avatarUrl: user.avatar
    })
  } catch (error) {
    console.log(error);
    return res.status(500).json({
      message: "Lỗi server"
    })
  }
}

module.exports.searchUser = async (req, res) => {
  try {
    const { username } = req.query;
  
    if(!username) {
      return res.status(400).json({ message: "Vui lòng cung cấp username để tìm kiếm!"});
    }

    const userId = req.user.id;
    const users = await User.find({
      _id: { $ne: userId }, // Loại trừ user hiện tại
      username: { $regex: username, $options: 'i'}
    }).select('-password')

    return res.status(200).json({
      message: "Tìm kiếm thành công!",
      users
    })

  } catch (error) {
    console.log("Search user error:", error);
    return res.status(500).json({ message: "Lỗi server khi tìm kiếm user!"});
  }
}